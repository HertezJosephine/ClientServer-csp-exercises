            <h2>About</h2>
            <hr class="bottom-line">
            <p><i>"The Archdiocesan Shrine and Parish of St. Jude Thaddeus in Concepcion Grande, Naga City, Philippines, is known for its elegant yet simple interior and eye-soothing landscape, making it almost always a first choice for a wedding venue for many young couples desiring to tie the knot."</i></p><br>
            <p class="text-justify"><b>Our Story</b> <br><br>

            The Parish of St. Jude Thaddeus in Concepcion Grande, Naga City, was canonically erected on <b><i>June 28, 1997</i></b>, upon the recommendation of Rev. Msgr. Juan Alarcon, then parish priest of the Immaculate Conception Parish, and upon the the advice of Rev. Fr. Rogelio Largoza, the Vicar Forane of St. John the Evangelist Parish. From then on, the parish has been an important center of Catholic faith in this heavily residential area of Naga City. <br><br> 

            Rev. Msgr. Jose Roberto Z. Carpio, a former resident formator of the Holy Rosary Major Seminary was installed as the <b><i>first Parish Priest</i></b>. <br><br>

            The creation of the parish was occasioned by the reasonable spiritual and pastoral needs of barangays Concepcion Grande and Del Rosario, which were formerly within the ecclesiastical jurisdiction of the Immaculate Conception Parish. <br><br>

            Even before the establishment of the parish, Msgr. Alarcon had long been sensing the need for it in view of the rapidly increasing population in the area. He consequently proposed to the Archbishop of Caceres the creation of a new parish. <br><br>

            That time, Barangay Concepcion Grande had around 7,598 residents and was composed of the following sitios and Ssubdivisions: Villa Sorabella, Sitio Magdalena, Ramaida Village, Sitio Sapang Palay, BF Homes, Villa Grande Homes, and Almeda Village; while Del Rosario had around 5,021 residents and was composed of the following sitios and Subdivisions: Sitio Salvacion, Villa Corazon Subdivision, Do√±a Conchita, Monte Cielo and Jardin Real I and II. <br><br>

            The formal erection of the Parish and the installation of its parish priest took place on June 28, 1997, at the chapel of Villa Grande Homes, with Rev. Msgr. Benjamin Reblando, Chancellor of the Archdiocese of Caceres, reading the decree of erection. <br><br>

            St. Jude Thaddeus whose feast day is celebrated every 28th of October was also named titular patron of the parish. During the solemn Mass, Archbishop Leonardo Z. Legaspi installed Fr. Carpio as its first parish priest. The historical occasion was graced by the presence of the clergy, city officials and some prominent persons of the locality. <br><br>

            With its canonical erection, the new parish would consequently enjoy the rights and privileges of a parish as mandated by the Code and Archdiocesan statutes and approved customs. Some of those privileges are the habitual keeping of the Blessed Sacrament, possessing of baptismal font, administering Sacraments and enjoying other rights belonging to a parish church. <br><br>

            Providentially, the parish attained its dream and desired objective through the generosity of the <b><i>Almeda Family</i></b> who donated 3,000-square meter lot in Almeda Village. The donated space for the church proved ideal as the place of the parish church as it sits between the two barangays. <br><br>

            The construction work ran smoothly and with facility with the professional assistance of Architect Benedicto Reyes who offered his service generously. Fund drives, including tithes, pledges, donations from goodhearted parishioners and devotees, were initiated to defray the construction expenses. With the aid and loan from the Archbishop, the church construction began on February 28, 1999. <br><br>

            The first to be blessed was the parish rectory which was completed on October 28, 1999, in time for the parish fiesta. On the parish fiesta of the following year, the main altar and the parish hall were consecrated by the Archbishop. <br><br>

            Today, the Parish of St. Jude Thaddeus in Concepcion Grande is known for its elegant yet simple interior and eye-soothing landscape, making it almost always a first choice for a wedding venue for many young couples desiring to enter married life.</p> <br>
